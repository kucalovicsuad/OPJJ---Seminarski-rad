    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if (!itemNameInput.getText().isEmpty() || !amountInput.getText().isEmpty() || !purchasePriceinput.getText().isEmpty() || !sellingPriceInput.getText().isEmpty()) {
            Item newItem = new Item();
            
            newItem.setId_distributor(Integer.parseInt(distributorInput.getSelectedItem().toString().split("\\s+")[0]));
            newItem.setNaziv(itemNameInput.getText());
            newItem.setKolicina(Integer.parseInt(amountInput.getText()));
            newItem.setPopust(Double.parseDouble(discountInput.getText()));
            newItem.setCijena_nabavke(Double.parseDouble(purchasePriceinput.getText()));
            newItem.setCijena_prodaje(Double.parseDouble(sellingPriceInput.getText()));
            
            try {
                if (itemController.addNewItem(newItem)) {
                    JOptionPane.showMessageDialog(rootPane, "Uspje≈°no dodan novi artikal.", "Obavijest", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    SQLException ex = new SQLException();
                    throw ex;
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Gre≈°ka tokom dodavanja artikla.", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Molimo popunite sva polja.", "Gre≈°ka", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addBtnActionPerformed


